        /*
        \b      backspace
        \t      tab
        \n      New Line
        \r      carriage return. ()
        \f      form feed
        \'      Inserts a single quote
        \ "     Inserts a double quote
        \\      Inserts a backl slash
         */

        System.out.println("2 \t 2");
        System.out.println("3 \t 4");
        System.out.println("3333 \t 4333 \t 456789 \t 98765");
